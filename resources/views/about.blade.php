                                                         <div class="ee-body-content">
                                                            <p>The history of Ministry of Foreign Affairs and East African Cooperation dates back soon after independence in 1961. Up to December 1963, it was a department under Prime Minister’s Office.&nbsp;In 1964 it became a fully fledged Ministry under the name of Ministry of External Affairs.</p><p>For four decades since the sixties, the Ministry’s operations were guided by Presidential Circular No. 2 of 1964. The Circular specified Foreign Policy priorities that evolved around racial equality, collective self-reliance, world at peace, on the basis of the socio-economic realities of sixties and seventies. For instance, the challenges of colonial domination and neo-colonialism, the super power rivalry, unfavorable economic situation of developing countries, and the fragility of statehood in Africa - all demanded such response. The policy served Tanzania well over the years.</p><p>However, both domestic and international economic and social political shifts in the eighties and nineties necessitated the revision of Foreign Policy and as a consequence the redefinition of the Ministry’s roles and functions. Specific contributory factors included the disintegration of the bipolar system of international relations and the whole range of major political and economic consequences that followed. For example, the global shift towards market - led economies and the process of regionalization, globalization and liberalization, the shift from development aid to trade and investments, the resurgence of intra-state and inter-state conflicts, the movement towards greater embrace of multi-party democracy, human rights and good governance, the emergence of new shared challenges such as international terrorism, illegal immigration, the preservation of the environment, advances in science and technology, fundamentally altered the international system.</p><p><strong>Vision</strong><br>- To become an effective promoter of Tanzania's economic and other national interests abroad.</p><p><strong>Mission</strong><br>- To conduct an effective diplomacy which will generate economic activity and facilitate Tanzania’s rapid transformation and sustainable development.</p> 
